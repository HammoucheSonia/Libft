#include <stddef.h>
#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	while (dst[i])
		i++;
	while (src[j])
		j++;
	if (size < i)
		return (j + size);
	else
	{
		dst = dst + i;
		ft_strlcpy(dst, src, size - i);
		return (i + j);
	}
}
